local IsFull = false
local Reloadtime = 0
function SWEP:Reload() 
	if Reloadtime <= CurTime() then
	if IsFull then
		self:SendWeaponAnim(ACT_VM_THROW)
		timer.Simple(0.9,function()
			if self.Owner:GetActiveWeapon() == self then
				self:SendWeaponAnimTime( ACT_VM_IDLE , 0.2 )
			end
		end)
		IsFull = false
	else
		self:SendWeaponAnim(ACT_VM_RELOAD)
		IsFull = true
	end
	Reloadtime = CurTime() + 1
	end
	--[[
	if CLIENT then
		self:RunCommand("cd /") 
	end
	]]
end

function SWEP:SendWeaponAnimTime(anim,time)
	time = tonumber(time) or 0.5
	self:SendWeaponAnim( anim )
	self.Owner:GetViewModel():SetPlaybackRate( time )
	self.Owner:GetViewModel():SetAnimation(anim)
end

